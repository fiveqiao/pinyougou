//购物车控制层
app.controller('cartController', function ($scope, cartService) {
//查询购物车列表
    $scope.findCartList = function () {
        cartService.findCartList().success(function (response) {
            $scope.cartList = response;
            $scope.totalValue = cartService.sum($scope.cartList);//求合计
        });
    }
    $scope.addGoodsToCartList = function (itemId, num) {
        cartService.addGoodsToCartList(itemId, num).success(function (response) {
            if (response.success) {
                $scope.findCartList();//刷新列表
            } else {
                alert(response.message);//弹出错误提示
            }
        });
    }
    //获取地址列表
    $scope.findAddressList = function () {
        cartService.findAddressList().success(function (response) {
            $scope.addressList = response;
            //设置默认地址
            for (var i = 0; i < $scope.addressList.length; i++) {
                if ($scope.addressList[i].isDefault == '1') {
                    $scope.address = $scope.addressList[i];
                    break;
                }
            }
        });
    }
    //选择地址
    $scope.selectAddress = function (address) {
        $scope.address = address;
    }
    //判断是否是当前选中的地址
    $scope.isSelectedAddress = function (address) {
        if (address == $scope.address) {
            return true;
        } else {
            return false;
        }
    }

    $scope.order = {paymentType: '1'};
    //选择支付方式
    $scope.selectPayType = function (type) {
        $scope.order.paymentType = type;
    }

    //保存订单
    $scope.submitOrder = function () {
        $scope.order.receiverAreaName = $scope.address.address;
        //地址
        $scope.order.receiverMobile = $scope.address.mobile;
        //手几
        $scope.order.receiver = $scope.address.contact;//联系人
        cartService.submitOrder($scope.order).success(function (response) {
            if (response.success) {//页面跳转
                if ($scope.order.paymentType == '1') {//如果是微信支付，跳转到支付页面
                    location.href = "pay.html";
                } else {//如果货到付款，跳转到提示页面
                    location.href = "paysuccess.html";
                }
            } else {
                alert(response.message); //也可以跳转到提示页面
            }
        });
    }

    //定义键盘回车事件
    $scope.enterEvent = function (itemId, num) {
        if (num >= 200) {
            num = 200;
        }
        cartService.addGoodsToCartList(itemId, num).success(function (response) {
            if (response.success) {
                $scope.findCartList();//刷新列表
            } else {
                alert(response.message);//弹出错误提示
            }
        });
    }
    //增加收货地址
    $scope.addAddress = function () {
        cartService.addAddress($scope.entity).success(
            function (response) {
                if(response.success){
                    //添加成功刷新页面
                    $scope.findAddressList();
                }else {
                    //失败弹出框
                    alert(response.message);
                }
            })
    }
});

